#A class which stores some constants to decorate console outputs
class ConsoleMessages():
    ERROR       = "\033[1;31mERROR:"
    WARNING     = "\033[1;31mWARNING:"
    NORMAL      = "\033[1;0m"
    BOLD        = "\033[1;1m"
    INFO        = "\033[1;33m"
    BACKED_P    = "\033[1;37;45m"
    BACKED_C    = "\033[1;37;46m"
